# --- !Ups

CREATE TABLE Messages (
  message VARCHAR NOT NULL,
  toUserId BIGINT NOT NULL, 
  fromUserId BIGINT NOT NULL, 
  id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY);

CREATE TABLE Transactions (
  storageTaken INT NOT NULL,
  fromDate TIMESTAMP NOT NULL,
  toDate TIMESTAMP NOT NULL,
  timestamp TIMESTAMP NOT NULL,
  postingId BIGINT NOT NULL,
  buyerUserId BIGINT NOT NULL, 
  fromUserId BIGINT NOT NULL, 
  id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY);

CREATE TABLE User (
  userID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR NOT NULL,
  surname VARCHAR NOT NULL,
  email VARCHAR NOT NULL UNIQUE,
  password VARCHAR NOT NULL,
  verifiedEmail SMALLINT DEFAULT 0,
  thumbnail VARCHAR,
  universityID BIGINT NOT NULL,
  creationTime BIGINT NOT NULL,
  lastLogin BIGINT);

CREATE TABLE Post (
  postID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  email VARCHAR NOT NULL,
  description VARCHAR NOT NULL);

CREATE VIEW VeridifedUser AS
  SELECT * 
  FROM User
  WHERE verifiedEmail = 1;

